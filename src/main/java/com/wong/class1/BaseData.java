package com.wong.class1;

public class BaseData {
    public static void main(String[] args) {
        //声明变量
        byte a;
        short s;
        int i;
        long l;
        //声明变量同时赋予初始值
        byte fb = 1;
        //同时声明多个变量
        int ia, ib;
        //使用时赋值
        ia = 1;
        //同时声明多个并赋予变量值
        int iia = 3, iib = 4;
        System.out.println(fb);
        System.out.println(iia);
         //浮点数，默认是double类型
        //单精度float
        float fa = 1.66666666666666f;
        //双精度double
        double da = 1.6666666666666f;
        System.out.println(fa);
        System.out.println(da);
    }
}
